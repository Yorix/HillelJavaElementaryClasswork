package com.yorix.hillel.java_elementary.lesson26.lambda;

public class Person {
    private String name;
    private double rate;

    public Person(String name, double rate) {
        this.name = name;
        this.rate = rate;
    }

    public String getName() {
        return name;
    }

    public double getRate() {
        return rate;
    }
}
